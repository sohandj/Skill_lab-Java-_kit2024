package day_2;

public class Size {

	public static void main(String[] args) {
		System.out.println("Long min value :" + Long.MIN_VALUE);
		System.out.println("Long max value :" + Long.MAX_VALUE);
		System.out.println("Long size :" + Long.BYTES);
		System.out.println(" ");
		System.out.println("Double min value :" + Double.MIN_VALUE);
		System.out.println("Double max value :" + Double.MAX_VALUE);
		System.out.println("Double :" + Double.BYTES);
		System.out.println(" ");
		System.out.println("Short min value :" + Short.MIN_VALUE);
		System.out.println("Short max value :" + Short.MAX_VALUE);
		System.out.println("Short size :" + Short.BYTES);
		System.out.println(" ");
		System.out.println("Long min value :" + Integer.MIN_VALUE);
		System.out.println("Long max value :" + Integer.MAX_VALUE);
		System.out.println("Long size :" + Integer.BYTES);
		System.out.println(" ");
		
		// does not work for char ,boolean

	}

}
